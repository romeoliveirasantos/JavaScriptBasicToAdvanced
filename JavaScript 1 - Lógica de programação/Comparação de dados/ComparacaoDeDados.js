// operadores de comparação

// == igual
console.log(5 == "5")

// === idêntico (igualdade estrita)
console.log( 5 === "5")

// != diferente
console.log(5 != "5")

// !=== diferente idêntico (desigualdade estrita)
console.log(5 !== "5")

// > maior
console.log(5 > 5)

// >= maior igual
console.log(5 >= 5)

// < menor
console.log(5 < 5)

// <= menor igual
console.log(5 <= 5)



